/* Sentencia while*/
package contadorhasta10;
public class ContadorHasta10 {
public static void main(String[] args) {
	int cont=0;
	while(cont<10) {
		System.out.println(cont);
		cont=cont+1;
	}
System.out.print("Final del while");
}
}

